    func alertFunc(){
        let alertVC = UIAlertController(
            title: "Alert Title",
            message: "Alert Message",
            preferredStyle: .alert)
        let okAction = UIAlertAction(
            title: "OK",
            style:.default,
            handler: nil)
        alertVC.addAction(okAction)
        present(
            alertVC,
            animated: true,
            completion: nil)
    }